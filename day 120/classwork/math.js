const add = (num1, num2) => {
    return num1 + num2;
};

const minus = (num1, num2) => {
    return num1 - num2;
};

const multiply = (num1, num2) => {
    return num1 * num2;
};

const divide = (num1, num2) => {
    return num1 / num2;
};


module.exports = {
    add,
    minus,
    multiply,
    divide,
};
